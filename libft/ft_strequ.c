int		ft_strequ(char const *s1, char const *s2)
{
	int a;

	a = 0;
	while (s1 && s2 && s1[a] == s2[a])
	{
		if (s1[a] + s2[a] == 0)
			return (1);
		a++;
	}
	return (0);
}
