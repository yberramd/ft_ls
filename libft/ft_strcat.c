#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int dl;
	int i;

	i = 0;
	dl = ft_strlen(s1);
	while (s2[i] != '\0')
	{
		s1[dl + i] = s2[i];
		i++;
	}
	s1[dl + i] = '\0';
	return (s1);
}
