#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t a;

	a = 0;
	if (!n)
		return (1);
	while (s1 && s2 && s1[a] == s2[a])
	{
		if (s1[a] + s2[a] == 0 || a == n - 1)
			return (1);
		a++;
	}
	return (0);
}
